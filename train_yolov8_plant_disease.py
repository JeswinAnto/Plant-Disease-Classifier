def check_gpu():
    """Check GPU availability and memory"""
    if torch.cuda.is_available():
        device = torch.cuda.current_device()
        gpu_name = torch.cuda.get_device_name(device)
        total_memory = torch.cuda.get_device_properties(device).total_memory / 1e9
        print(f"✓ GPU Available: {gpu_name}")
        print(f"✓ Total Memory: {total_memory:.2f} GB")
        return True
    else:
        print("⚠ No GPU available, using CPU")
        return False


def verify_dataset(dataset_root, data_yaml_path):
    """Verify dataset structure and data.yaml"""
    print("\n" + "="*70)
    print("DATASET VERIFICATION")
    print("="*70)
    
    dataset_path = Path(dataset_root)
    
    if not dataset_path.exists():
        raise FileNotFoundError(f"Dataset not found at {dataset_root}")
    
    # Check for data.yaml
    yaml_path = Path(data_yaml_path)
    if not yaml_path.exists():
        print(f"✗ data.yaml not found at {data_yaml_path}")
        print(f"\nGenerating data.yaml automatically...")
        
        # Auto-generate yaml
        try:
            from generate_data_yaml import generate_data_yaml
            generated_yaml = generate_data_yaml(dataset_root, data_yaml_path)
            print(f"✓ Generated: {generated_yaml}")
        except Exception as e:
            print(f"\n✗ Failed to auto-generate data.yaml: {str(e)}")
            print(f"\nPlease run manually:")
            print(f"  python generate_data_yaml.py '{dataset_root}'")
            raise
    else:
        print(f"✓ Found data.yaml at {data_yaml_path}")
    
    # Check for train and valid directories
    train_path = dataset_path / 'train'
    valid_path = dataset_path / 'valid'
    
    if not train_path.exists():
        raise FileNotFoundError(f"Training directory not found at {train_path}")
    
    # Count classes and images
    train_classes = [d for d in train_path.iterdir() if d.is_dir()]
    num_train_classes = len(train_classes)
    
    # Count images in each split
    train_images = sum([len(list(cls_dir.glob('*.JPG'))) + len(list(cls_dir.glob('*.jpg'))) + 
                        len(list(cls_dir.glob('*.jpeg'))) + len(list(cls_dir.glob('*.png'))) 
                        for cls_dir in train_classes])
    
    print(f"✓ Dataset root: {dataset_root}")
    print(f"✓ Training classes: {num_train_classes}")
    print(f"✓ Training images: {train_images}")
    
    if valid_path.exists():
        valid_classes = [d for d in valid_path.iterdir() if d.is_dir()]
        valid_images = sum([len(list(cls_dir.glob('*.JPG'))) + len(list(cls_dir.glob('*.jpg'))) + 
                           len(list(cls_dir.glob('*.jpeg'))) + len(list(cls_dir.glob('*.png'))) 
                           for cls_dir in valid_classes])
        print(f"✓ Validation classes: {len(valid_classes)}")
        print(f"✓ Validation images: {valid_images}")
    else:
        print("⚠ No validation directory found")
    
    # Print sample classes
    print(f"\nSample classes (first 10):")
    for i, cls_dir in enumerate(sorted(train_classes)[:10], 1):
        img_count = len(list(cls_dir.glob('*.JPG'))) + len(list(cls_dir.glob('*.jpg'))) + \
                   len(list(cls_dir.glob('*.jpeg'))) + len(list(cls_dir.glob('*.png')))
        print(f"  {i}. {cls_dir.name}: {img_count} images")
    
    return num_train_classes, train_images


