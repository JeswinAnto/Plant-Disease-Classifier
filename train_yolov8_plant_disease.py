def train_yolov8(config):
    """
    Train YOLOv8 classification model
    
    Args:
        config: Configuration object
    
    Returns:
        YOLO model and results
    """
    print("\n" + "="*70)
    print("INITIALIZING YOLOV8 TRAINING")
    print("="*70)
    
    # Initialize model
    print(f"\nLoading model: {config.MODEL_NAME}")
    model = YOLO(config.MODEL_NAME)
    
    # Setup training arguments
    train_args = {
        'data': config.DATA_YAML,  # Use data.yaml path instead of directory
        'epochs': config.EPOCHS,
        'imgsz': config.IMG_SIZE,
        'batch': config.BATCH_SIZE,
        'device': config.DEVICE,
        'workers': config.WORKERS,
        'optimizer': config.OPTIMIZER,
        'lr0': config.LR0,
        'lrf': config.LRF,
        'momentum': config.MOMENTUM,
        'weight_decay': config.WEIGHT_DECAY,
        'patience': config.PATIENCE,
        'save': config.SAVE,
        'save_period': config.SAVE_PERIOD,
        'val': config.VAL,
        'plots': config.PLOTS,
        'amp': config.AMP,
        'fraction': config.FRACTION,
        'project': config.PROJECT_ROOT,
        'name': f'plant_disease_classification_{datetime.now().strftime("%Y%m%d_%H%M%S")}',
        'exist_ok': True,
        'pretrained': True,
        'verbose': True,
    }
    
    # Add optional arguments
    if config.RESUME and config.RESUME_PATH:
        train_args['resume'] = config.RESUME_PATH
    
    if config.FREEZE is not None:
        train_args['freeze'] = config.FREEZE
    
    # Print training arguments
    print("\nTraining arguments:")
    for key, value in train_args.items():
        print(f"  {key}: {value}")
    
    print("\n" + "="*70)
    print("STARTING TRAINING")
    print("="*70)
    
    # Start training
    results = model.train(**train_args)
    
    print("\n" + "="*70)
    print("TRAINING COMPLETE!")
    print("="*70)
    
    return model, results


# ============================================================================
# VALIDATION FUNCTION
# ============================================================================

def validate_model(model, config):
    """
    Validate trained model
    
    Args:
        model: Trained YOLO model
        config: Configuration object
    
    Returns:
        Validation results
    """
    print("\n" + "="*70)
    print("VALIDATING MODEL")
    print("="*70)
    
    # Validation arguments
    val_args = {
        'data': config.DATA_YAML,  # Use data.yaml instead of directory
        'imgsz': config.IMG_SIZE,
        'batch': config.BATCH_SIZE,
        'device': config.DEVICE,
        'workers': config.WORKERS,
        'plots': True,
        'verbose': True,
    }
    
    # Run validation
    results = model.val(**val_args)
    
    print("\n" + "="*70)
    print("VALIDATION COMPLETE!")
    print("="*70)
    
    return results


# ============================================================================
# EXPORT FUNCTION
# ============================================================================

def export_model(model, config):
    """
    Export model to different formats
    
    Args:
        model: Trained YOLO model
        config: Configuration object
    """
    print("\n" + "="*70)
    print("EXPORTING MODEL")
    print("="*70)
    
    export_formats = ['onnx', 'torchscript']
    
    for fmt in export_formats:
        try:
            print(f"\nExporting to {fmt.upper()}...")
            model.export(format=fmt, imgsz=config.IMG_SIZE)
            print(f"✓ Successfully exported to {fmt.upper()}")
        except Exception as e:
            print(f"✗ Failed to export to {fmt.upper()}: {str(e)}")
    
    print("\n" + "="*70)
    print("EXPORT COMPLETE!")
    print("="*70)


